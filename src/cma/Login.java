    public void verify() throws ClassNotFoundException{
                uname = jTextField1.getText();
                password = jPasswordField1.getText();
                int flag=0;
                
            try{
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            String CMA = "jdbc:derby://localhost:1527/CMA";
            Connection DBconn = DriverManager.getConnection(CMA, "cma","cma");
            
              Statement st = DBconn.createStatement();
              ResultSet rs=st.executeQuery("SELECT username,password FROM LOGIN");
           
              while (rs.next()) 
                {
                 String dbUsername = rs.getString("username");
                 String dbPassword = rs.getString("password");

                
                if(dbUsername.equals(uname) && dbPassword .equals(password)){
                    flag=1;
                    break;
                }
                else
                    flag=0;
                }
              
                if(flag==1)
                    new MainForm().setVisible(true);
                 
                else
                    JOptionPane.showMessageDialog(null,"Invalid Username or Password", "Error Message", JOptionPane.OK_OPTION);

                

        }
         catch(SQLException err)
            {
                System.out.println(err.getMessage()); 
            } 

                
    }

